function getExclusiveAddresses(existedAddresses: string[], addresses: string[]) {
    const intersection: string[] = [];

    const exclusiveAddresses = addresses.filter((address) => {
        if (existedAddresses.includes(address)) {
            intersection.push(address);
            return false;
        }
        return true;
    });

    return [exclusiveAddresses, intersection];
}

